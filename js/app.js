/** Sum of per-trail incident counts in dataset within radius (geographic data-density proxy). */
const MIN_REGION_INCIDENT_RECORDS = 15;
const INC_COVERAGE_RADIUS_KM = 40;

function incidentRecordSumWithinRadius(t) {
  if (!t || !TRAILS.length) return 0;
  const R = INC_COVERAGE_RADIUS_KM;
  let s = 0;
  for (const o of TRAILS) {
    if (kmAway(t.lat, t.lng, o.lat, o.lng) <= R) s += Math.max(0, parseInt(o.inc, 10) || 0);
  }
  return s;
}

function isLimitedDataZone(t) {
  if (!t) return true;
  return incidentRecordSumWithinRadius(t) < MIN_REGION_INCIDENT_RECORDS;
}

