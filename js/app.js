/**
 * When true, do not show region incident counts as if they were strong statistics.
 * OSM-derived paths: no trail-specific history in our dataset. CSV: 0 incidents = sparse.
 */
function isSparseRiskData(t) {
  if (t && t.source === 'osm') return true;
  const inc = t && t.inc != null ? parseInt(t.inc, 10) : 0;
  if (Number.isNaN(inc) || inc < 1) return true;
  return false;
}

/** Secondary line under every risk score: data / history confidence (not a go/no-go). */
function riskConfidenceLine(t) {
  const m = t.updatedMin != null ? Math.max(0, parseInt(t.updatedMin, 10) || 0) : 0;
  const src = t && t.source === 'osm' ? 'OpenStreetMap path data + live weather feed' : 'CSV history + live weather feed';
  if (isSparseRiskData(t)) {
    return `Uncertainty: High | Confidence: Low | Data source: ${src} | Weather timestamp: ${m} min ago.`;
  }
  const n = t.inc != null ? parseInt(t.inc, 10) || 0 : 0;
  return `Uncertainty: Medium | Confidence: Moderate | Data source: ${src} | Weather timestamp: ${m} min ago | Incident records: ${n}.`;
}

/** Human-readable tier lines — avoid “safe / danger” verdict phrasing. */
function statusTierUserLine(tierKey) {
  switch (String(tierKey).toLowerCase()) {
    case 'safe':
      return 'Estimated conditions: Low';
    case 'caution':
      return 'Estimated conditions: Moderate';
    case 'risk':
      return 'Estimated conditions: Elevated';
    case 'danger':
      return 'Elevated risk indicators detected — see details';
    default:
      return 'Estimated conditions: Moderate';
  }
}

function riskToStatusKey(r) {
  if (r <= 3) return 'safe';
  if (r <= 5.5) return 'caution';
  if (r <= 7.5) return 'risk';
  return 'danger';
}

